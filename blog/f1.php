  <h2>Boys fashion these days</h2>
  <p>As Men’s Fashion Weeks come to a close, the trends are only beginning to surface on the streets. From patchwork prints to oversized suits, there is a myriad of new styles that flatter anyone’s taste. Whether you need a wardrobe update or you’re just searching for a bit of summer styling inspiration, check out the top ten men’s trends from S/S FW 2020.


  <h4>Cross-Body Bags</h4>
  <p>Keep your belongings safe and sound with a super stylish crossbody bag. Whether it be a messenger-style or something small and neat, these are a great way to secure your stuff and do it fashionably. If the strap is too long for your liking, tie a small knot in it, and you’ll have a unique addition to your collection. This is a great way to jazz up an outfit and is a practical way to do it.</p>


  
  <h4>Cuban Collar Shirts</h4>
  <p>Feel the summer breeze in a stylish Cuban collar shirt. With a distinctive collar and short sleeves, they’re an easy way to jazz up an outfit and make a real statement. You can choose to wear them with a bold print or keep it plain in a solid color. These shirts look extra cool in bright shades like yellow, blue or green. Complete the look with chinos and sunglasses, and you’ll be donning the summer sun in style.
</p>


    <h4>Flares</h4>
    <p>The ’70s are back in a big way with these flares. With a thin upper leg and an exaggerated ankle, these trousers are sure to make an impact anywhere you go. Pair the pants with a slim-fitting shirt or turtleneck to emphasize the bell bottom, or keep it flowing with an oversized hoodie and T-shirt for a more casual feel. No matter how you style these, you’ll have the appearance of super long and oh-so-stylish pins.</p>

    <h4>Over the Knee Shorts</h4>
    <p>Achieve the ultimate casual feel in a pair of over the knee shorts. These are a great way to keep yourself mostly covered but stay cool in the summer heat. Whether you rock these bottoms with a bold pattern or you keep it hot in a bright color, you can’t go wrong in these statement pieces. Complete this look with an open shirt and fresh kicks.</p>


