    <h2>Relationship Goals For Couples In Modern Times</h2>
    <h4>Respect</h4>
    <p>One must not forget that ‘Every individual is an institution in themselves’. It could be the case that perspectives differ. Perspectives are a function of one’s experiences and sufferings. Thus one has to respect the other’s perspective too. </p>

    <h4>Cherish</h4>
  <p>Every person holds within themselves both positive and negative attributes. The key to happiness is in cherishing the positives. Negatives if inconsequential must be ignored altogether, and effort shall be made to provide feedback on other shortcomings.</p>

  <h4>Nourish</h4>
  <p>Most successful relationships are those where partners encourage each other to follow their dreams, develop new skills and expand their knowledge. The success need not be measured in terms of personal success only. Sometimes, success of your partner should feel more accomplishing.</p>

  <h4>Sharing </h4>
  <p>A confused mind may only look forward to sharing the benefits of a partnership. But a wise mind is that who also shares the responsibilities. Sharing the load paves the path for willful sharing of the good times and the benefits that ensue.</p>

  <h4>Spare Time For Each Other</h4>
  <p>In the rut of daily life, we get so busy completing the chores that we forget to actually cherish each other’s presence. Take a stand for each other and give that time exclusively to yourself as a couple. Kids, parents, friends and family at large will be there, but it is your partner who shall be around to provide you that intimate comfort. Value them.</p>

  <h4>Break the Routine</h4>
  <p>Taking cue from the above point about sparing time for each other, the best way to do it is to break the routine once in a while. Ignoring home chores for a day will free you up to spend time with each other. And this extra time spent in each other’s company will automatically fuel you up for the challenges of familial life for a few days that lay ahead.</p>

