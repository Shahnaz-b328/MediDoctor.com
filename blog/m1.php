  <h2>Is It Time To Switch To A Menstrual Cup?</h2>
  <p>I distinctly remember sitting in a women’s reproductive health class in college (it was an easy elective!) learning for the first time that there was an alternative option to tampons and pads…the menstrual cup. My best friend and I looked at each other and immediately shook our heads. There was NO WAY we would ever be sticking our fingers up there and using a reusable menstrual product. In this moment I categorized anything that fell under the umbrella of menstrual cup into the “for hippies only” category and then moved on with my life.
  Fast forward 5 years later. I’m 26 and am struggling with my hormones. I’ve taken the pill, come off of it, switched to an IUD, come off of that and my body was not pleased. I had cystic acne on my face, back and chest and for the first time in my life I’m actually learning about my cycle and the hormones that control it. It was quite the wake-up call.
  I started to track my cycle, taking note of what changes I would see in my body throughout each stage and began learning more about ways to support my cycle holistically. I tried everything from seed cycling, to herbs, to adjusting my exercise routine to whatever stage of my cycle I was in. And for the first time I looked up what was in my beloved box of tampax.
  Dioxins
  Glyphosate
  Malaoxon
  Dichlofluanid
  Mecarbam

  …the list goes on

  I was shocked, to say the least. As someone who cares what I put in and on my body, it was mind-boggling to me that certain ingredients we would never allow in our food for being known human carcinogens were being used every single day by menstruating women. And so began my search for a better, cleaner tampon. Fortunately, these days there are organic and natural tampons available (LOLA is a great brand!) but the more I learned about tampons the more I realized how detrimental they are to the environment. So what started as a search for a cleaner tampon for my body led me to finding a cleaner menstrual product for the planet.

It’s been 4 years since I switched to a menstrual cup and honestly, I’ve never looked back.
  </p>

    
    <h2>So Why Switch To a Menstrual Cup?</h2>
    <p>As I mentioned above, there are two main reasons to switch to a menstrual cup.</p>
    <h4>1. It is a better choice for the environment</h4>
    <p>This is by far the biggest reason to switch. Many tampons and pads use plastic applicators or linings that are harmful to the environment. Much of this ends up in landfills and are not biodegradable. Even plastic-free or applicator-free tampons (which are marginally better) oftentimes end up in the landfill where they take a very long time to decompose. One single menstrual cup can replace up to 3-5 years worth of tampons and pads with no waste byproduct. And depending on where you live when it’s time to replace your menstrual cup, many can be recycled. </p>


    <h4>2. They are better for our bodies</h4>
    <p>Tampons and pads are viewed as medical devices in the US and are reviewed by the Food and Drug Administration. As a result, they are not required to list the ingredients on their packaging, which means unless it’s been third-party tested it’s hard to know what’s really in your menstrual products. Of course, there are many organic tampon and pad companies popping up that thankfully are being transparent about their ingredients but nonetheless, your safest bet is to go with a single-source product made from silicone (or rubber): the menstrual cup.</p>

    
