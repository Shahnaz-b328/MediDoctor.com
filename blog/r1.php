  <h2>High Protein Fried Rice</h2>
  <p>This delicious and satisfying fried rice serves as a perfect dinner or lunch recipe for those looking to eat clean without compromising on taste.There are two kinds of people in the food world; rice or bread lovers. If you prefer to the former then this dish will be wildly successful in your kitchen. Healthy, tasty and full of protein, it is the perfect dish after a heavy workout or simply when you wish to eat healthy. If you are concerned about whether eating rice will make you fat, then let me just put you at ease and say, no it wont.</p>


    <h4>Ingredients</h4>
    <p>
    1 small cup boiled brown rice<br>
    1 cup chopped and boiled carrots and peas<br>
    2 tbsp grounded flax seeds<br>
    2 teaspoons olive oil<br>
    2 egg whites scrambled ( cooked)<br>
    Salt and pepper<br>
    1 teaspoon apple cider vinegar
  </p>

  <h4>Method</h4>
  <p>
    Heat the olive oil in a wok.<br>
    Fry the carrots and peas for one minute<br>
    Add the eggs, flax-seeds, boiled rice and stir well.<br>
    Mix in this, apple cider vinegar.<br>
    Add salt and pepper and cook in low heat for 2 min.<br>
    Serve hot.

If you are a vegetarian, you can replace the egg with cubed tofu pieces. Just make sure you saute them first.</p>
