  <h2>Crispy and Delicious Roast Chicken</h2>
  <p>The easy dinner recipe that every home cook should have! This Crispy Roast Chicken with Vegetables looks impressive, tastes amazing, and nobody will ever guess that it only took you 15 minutes to pull together! At this time of year, my mind, body and soul crave the cozy comfort and the familiar smell of a chicken roasting in the oven. There’s just nothing better! I was raised on boneless, skinless chicken breasts, so dealing with a whole chicken intimidated me for many years. When I finally garnered the courage to tackle a whole roast chicken, there was no turning back. I realized that it’s just as easy as baking the boneless, skinless pieces — but the flavor, texture, and juiciness can’t be beat!</p>


    <h4>How do I make the best roast chicken?</h4>
    <p>First, you’ll take your whole chicken, remove the giblets, rinse it with water, and pat it dry with paper towels. Season the chicken cavity with salt and pepper, and then stuff the inside of the bird with lemon, celery, half of the herbs, and the whole garlic cloves.<br>Tie the legs together with kitchen string and tuck the wings underneath the bird (if you can).<br>Place the chicken (breast-side up) in a large roasting pan. You know that it’s breast-side up when the tips of the drumsticks are pointing UP and the chicken looks like the shape of a heart.<br>Drizzle or brush the melted butter over the outside of the chicken, and season liberally with salt and pepper. The butter on the outside helps to make sure that the exterior is perfectly crispy and golden brown, so definitely don’t skip that step!<br>Place potatoes, carrots and onion in a large Ziploc bag (for easy clean up). Add the olive oil to the bag, season with salt and pepper, and toss to coat.Arrange the vegetables in the roasting pan around the chicken, along with the remaining herbs. That’s it! Your chicken is ready for the oven!<br>You definitely want to leave your whole chicken uncovered while it roasts in the oven. This will allow the skin to get crispy and golden brown.During the cooking process, if your chicken starts to get too dark for your liking before it’s done cooking internally, you can loosely tent the chicken with foil to prevent further browning.
    </p>
