    raw papaya paste – fresh, raw papaya can be found in Indian stores. It’s very easy to make this paste – just chop some papaya along with the green skin and put in a blender with a little water. Whiz to get a smooth puree. Or you can also buy bottled papaya, again from any Indian or Pakistani grocery stores.<br>
