  <h2>What causes allergies?</h2>
  <p>A common scenario can help explain how allergies develop. A few months after the new cat arrives in the house, dad begins to have itchy eyes and episodes of sneezing. One of the three children develops coughing and wheezing. The mom and the other two children experience no reaction despite the presence of the cat. How can this occur?

The immune system is the body's organized defense mechanism against foreign invaders, particularly infections. Its job is to recognize and react to these foreign substances, which are called antigens. Antigens often lead to an immune response through the production of antibodies, which are protective proteins that are specifically targeted against particular antigens. These antibodies, or immunoglobulins (IgG, IgM, and IgA), are protective and help destroy a foreign particle by attaching to its surface, thereby making it easier for other immune cells to destroy it. The allergic person however, develops a specific type of antibody called immunoglobulin E, or IgE, in response to certain normally harmless foreign substances, such as cat dander, pollen, or foods. Other antigens, such as bacteria, do not lead to production of IgE, and therefore do not cause allergic reactions. Once IgE is formed, it can recognize the antigen and can then trigger an allergic response. IgE was discovered and named in 1967 by Kimishige and Teriko Ishizaka.
  </p>

    <h4>Who is at risk for allergies and why?</h4>
    <p>Allergies can develop at any age, but most food allergies begin at a young age, and many are outgrown. Environmental allergies can develop at any time. The initial exposure or sensitization period may even begin before birth. Individuals can also outgrow allergies over time. It is not fully understood why one person develops allergies and another does not, but there are several risk factors for allergic conditions. Family history, or genetics, plays a large role, with a higher risk for allergies if parents or siblings have allergies. There are numerous other risk factors for developing allergic conditions. Children born via Cesarean section have a higher risk of allergy as compared to children who are delivered vaginally. Exposure to tobacco smoke and air pollution increases the risk of allergy. Boys are more likely to be allergic than girls. Allergies are more common in westernized countries, and less common in those with a farming lifestyle. Timing of exposures to antigens, use of antibiotics, and numerous other factors, some of which are not yet known, also contribute to the development of allergies. This complicated process continues to be an area of medical research</p>

