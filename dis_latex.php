    <h4>What are common types of allergic conditions, and what are allergy symptoms and signs?</h4>
    <p>The parts of the body that are prone to allergic symptoms include the eyes, nose, lungs, skin, and gastrointestinal tract. Although the various allergic diseases may appear different, they all result from an exaggerated immune response to foreign substances in sensitive individuals. The following are brief descriptions of common allergic disorders.

Allergic rhinitis (hay fever)
Asthma 
Allergic eyes (conjunctivitis)
Eczema (atopic dermatitis)
Hives (urticaria)
Anaphylaxis

