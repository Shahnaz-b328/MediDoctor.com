    <h4>Ten warning signs and symptoms of Alzheimer's disease</h4>
    <p>The following list of warning signs include common symptoms of Alzheimer's disease. Individuals who exhibit several of these symptoms should see a physician for a complete evaluation.

    Memory loss (forgetting important dates or events)
    Difficulty performing familiar tasks (problems remembering the rules to a favorite game or driving to a familiar place)
    Problems talking with others or writing (For example, a person may struggle to find the right words for items or names of people or places.)
    Disorientation to time and place (for example, forgetting where they are, loosing track of the seasons, dates, and passage of time)
    Poor or decreased judgment (for example, poor hygiene or poor judgment when dealing with money or financial matters)
    Vision problems (problems reading or judging distances)
    Problems with solving problems or planning (for example, problems tracking regular bills or following familiar recipes)
Misplacing things (for example, a person put items in unusual places and then are not able to retrace their steps find them again)
Changes in mood, personality, or behavior
Loss of initiative or withdrawal from social or work activities
</p>

<h4>How do health care professionals diagnose allergies? What are the types of allergy testing?</h4>
    <p>The diagnosis of allergies begins with a detailed history and physical examination. Many people with allergies have other family members with allergic conditions. In addition to the history and exam, skin testing and sometimes blood work (specific IgE levels) can help with the diagnosis of allergies. There are several important considerations when interpreting the results of this testing:

    For environmental allergies such as pets, dust mites, pollen, and molds, skin prick testing is the best test to help with diagnosis of allergy. Blood work looking for the allergic antibody (IgE) is less sensitive and may miss some allergies.
    For food allergy testing, the most important part of the diagnosis is the health history. Skin testing or blood work (specific IgE testing) should only be ordered if the history is suggestive of food allergy. Without a suggestive history, food allergy skin testing and blood work is not very specific and has a high rate of false positive results.
    For food allergy testing, ordering skin testing or blood work (specific IgE testing) for broad panels of foods is discouraged, given the high rate of false positive results.
For drug allergy, the history is the most important element in diagnosis. The only antibiotic with validated skin testing is penicillin. Penicillin skin testing can be very helpful in determining if an individual is truly allergic to penicillin and related antibiotics. Blood work (specific IgE testing) is not particularly helpful for the diagnosis of drug allergies.
Sometimes, such as with food allergy and drug allergy, despite a detailed history and appropriate testing, the diagnosis of allergy remains unclear. In these situations, it is appropriate to consider a “graded challenge,” which is the “gold standard,” or the best test, for the diagnosis of allergies. A graded challenge should always be performed with an allergist in a setting equipped to manage a severe allergic reaction, such as anaphylaxis.

</p>

    



