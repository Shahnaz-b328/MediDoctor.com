  <p>A common scenario can help explain how allergies develop. A few 
  	months after the new cat arrives in the house, dad begins to have 
  	itchy eyes and episodes of sneezing. One of the three children 
  	develops coughing and wheezing. The mom and the other two children
  	 experience no reaction despite the presence of the cat. How can this
