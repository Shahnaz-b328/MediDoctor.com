The immune system is the body's organized defense mechanism against foreign
 invaders, particularly infections. Its job is to recognize and react to 
 these foreign substances, which are called antigens. Antigens often lead 
 to an immune response through the production of antibodies, which are
  protective proteins that are specifically targeted against particular
   antigens. These antibodies, or immunoglobulins (IgG, IgM, and IgA),
    are protective and help destroy a foreign particle by attaching to
     its surface, thereby making it easier for other immune cells to 
     destroy it. The allergic person however, develops a specific type of 
     antibody called immunoglobulin E, or IgE, in response to certain 
     normally harmless foreign substances, such as cat dander, pollen, or 
     foods. Other antigens, such as bacteria, do not lead to production
      of IgE, and therefore do not cause allergic reactions. Once IgE is 
      formed, it can recognize the antigen and can then trigger an allergic
       response. IgE was discovered and named in 1967 by Kimishige and 
       Teriko Ishizaka.
