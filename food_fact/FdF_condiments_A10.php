  <h2>What are blueberries good for?</h2>
  <p>Blueberries are distinctive in many ways. Not only are they the only naturally occurring true blue food, they are also one of the three fruits native to North America.1 There are more blueberry species native to North America than any other continent, with the main species being northern and southern highbush, lowbush, half-high and rabbiteye.2,3

Blueberries were a staple in the Native American diet. When the first colonists arrived, they were shown how to gather blueberries, dry them in the sun, store them for winter and perpetuate their growth. Today, the North American blueberry industry ships more than 500 metric tons of fresh blueberries to Japan every year, and over 100 metric tons to Iceland.4 Cultivated blueberries are usually mild and sweet, while wild varieties have a more tart flavor. However, both types are excellent for the nutritional benefits they provide.5

If you aren’t eating blueberries right away, you can freeze them for later use. The best way to do this is to spread them on a baking sheet, unwashed, in a single layer. Put them in the freezer, and once frozen, place them in a freezer bag. This keeps them from clumping together and absorbing unwanted moisture.6

Keep in mind, though, that blueberries may lose some of their nutritional value and flavor when frozen.7 Whether fresh or frozen, the pale, powder-like coating on blueberries called "bloom," should remain until just before eating, as it helps protect the fruit from damage and disease.8</p>

