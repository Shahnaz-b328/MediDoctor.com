    <h4>Health benefits of blueberries</h4>
    <p>The use of blueberries as a Native American folk medicine was a precursor to the extracts used today for a number of illnesses.9 Vitamins K and C provide the highest amount of vitamins in blueberries, whereas manganese is the most prominent mineral. The remaining nutrients in blueberries is fairly balanced between vitamin E, thiamin, riboflavin and vitamin B6.10,11

Phytonutrients also account for the health benefits of blueberries. Among these are anthocyanins, which are bioactive flavonoid compounds that give blueberries their vivid color and are also known for their natural ability to neutralize free radicals.12 Other beneficial compounds in blueberries include pterostilbene,13 procyanidins, chlorogenic acid, quercetin, myricetin and resveratrol, among others. These compounds exhibit antioxidant and anti-inflammatory properties.14

The compounds in blueberries may help lower the risk for cancer,15 particularly breast cancer, colon cancer, esophageal cancer and cancer of the small intestine.16,17 Blueberries may also help improve cardiovascular health, raise good cholesterol levels, lower blood pressure,18 improve insulin resistance19 and lower the risk for macular degeneration.20 For more information about the nutritional content of blueberries, check out the nutrition facts table below</p>

