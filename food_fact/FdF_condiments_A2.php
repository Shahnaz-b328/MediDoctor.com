  <h2>What Is Tahini Good For?</h2>
  <p>Sesame seeds are often sprinkled on soups, salads and main dishes for garnishing or to add a slightly nutty taste. But did you know that these versatile seeds can also be used to make a rich paste that complements hummus, falafel and pita? This sesame seed paste is known as tahini, and it’s an essential supporting ingredient in the Middle Eastern, Greek, North African and Turkish cuisine.

Whether you’ve already tasted tahini or it’s the first time you’ve heard of it, you should familiarize yourself with this ingredient to find out the health benefits that it provides and how you can integrate it into your diet.

</p>


    <h4>Health Benefits of Tahini</h4>
    <p>This little berry's list of attributes includes a high level of antioxidant activity similar to cranberries, but more than what's been found in blueberries and strawberries. Oxygen Radical Absorbance Capacity (ORAC) values measuring the antioxidant power of acai fruit pulp/skin powder reportedly have the highest ORAC value among fruits and vegetables, or 10 times more antioxidants than red grapes.

Acai berries are low in sugar, but contain excellent amounts of calcium, fiber, and vitamin A. They also contain anthocyanin compounds such as resveratrol and cyaniding and ferulic acid, which not only give fruits and vegetables their distinct color, but also team up with flavonoids to defend the body against harmful free radicals. In fact, acai berries contain 10 to 30 times more anthocyanin power than red wine. Beneficial fatty acids such as oleic acid, one of the same oils found in olive oil, is another strong point, and healthy levels of dietary fiber keep the system functioning smoothly.

The properties contained in acai berries may help prevent health problems such as arthritis, inflammation, obesity, erectile dysfunction, neurological diseases, and allergies. Lab studies have shown them to have positive effects on ailments associated with oxidative stress, heart disease, and cancer.

Acai berries should be consumed in moderation, because they still contain sugar, even if in lower levels than other fruits. Remember, fructose may be harmful to your health in excessive amounts.</p>

