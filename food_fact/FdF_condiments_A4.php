  <h2>What Is Mirin Good For?</h2>
  <p>If you’ve ever traveled to Japan or eaten at an authentic Japanese restaurant, you’ll notice that their savory, umami-flavored dishes — teriyaki, for example — often have a hint of a delicate sweetness.

The first thing that may come to mind is sugar, but you’ll be surprised to know that the flavor probably comes from a type of rice wine — mirin, to be exact.

While drinking wine or using it for cooking is not advisable, it’s important to shed light on this popular condiment, which is an integral part of Asian cuisine. Keep reading to learn about mirin: its uses, how it’s made and why Japanese cooking just isn’t the same without it.</p>



    <h4>Health Benefits of Mirin</h4>
    <p>This little Mirin list of attributes includes a high level of antioxidant activity similar to cranberries, but more than what's been found in blueberries and strawberries. Oxygen Radical Absorbance Capacity (ORAC) values measuring the antioxidant power of acai fruit pulp/skin powder reportedly have the highest ORAC value among fruits and vegetables, or 10 times more antioxidants than red grapes.

Acai berries are low in sugar, but contain excellent amounts of calcium, fiber, and vitamin A. They also contain anthocyanin compounds such as resveratrol and cyaniding and ferulic acid, which not only give fruits and vegetables their distinct color, but also team up with flavonoids to defend the body against harmful free radicals. In fact, acai berries contain 10 to 30 times more anthocyanin power than red wine. Beneficial fatty acids such as oleic acid, one of the same oils found in olive oil, is another strong point, and healthy levels of dietary fiber keep the system functioning smoothly.

The properties contained in acai berries may help prevent health problems such as arthritis, inflammation, obesity, erectile dysfunction, neurological diseases, and allergies. Lab studies have shown them to have positive effects on ailments associated with oxidative stress, heart disease, and cancer.

Acai berries should be consumed in moderation, because they still contain sugar, even if in lower levels than other fruits. Remember, fructose may be harmful to your health in excessive amounts.</p>

