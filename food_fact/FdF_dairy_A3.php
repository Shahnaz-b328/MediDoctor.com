  <h2>What is Cottage-Cheese Good for</h2>
  <p>Similar to a cross between a grape and a blueberry, the acai (pronounced ah-sigh-ee) berry is a small, reddish-purple drupe consisting of a cluster of seeds, with only around 15 percent or so being edible, harvested from tall, slender palm trees found around the Amazon River basin of South America. These berries are also quite perishable, but have significant nutritional attributes when eaten fresh.

Traditional uses for acai fruit has included treatment for diarrhea, parasitic infections, hemorrhages and ulcers. As a food, acai pulp in the tribal Amazon belt is often blended with the starchy root vegetable manioc and eaten as porridge.

The taste is often described as reminiscent of wild berries and chocolate. It's found in large supermarkets and health food stores throughout the world, usually as a juice or tea rather than fresh, simply because getting them out of the Amazon with the nutrients still intact is a complicated process.

Other uses for the acai berry include natural food coloring, cosmetics, anti-aging skin creams, shampoos and conditioners, and also in food supplements. Expensive acai-based products have been widely hyped as having significant weight loss potential, but on that score, scientifically speaking, the jury's still out. In fact, the Federal Trade Commission announced in early 2012 that it had asked federal courts to temporarily suspend the activity of websites marketing acai berry weight loss products.</p>


    <h4>Health Benefits of Cottage-Cheese</h4>
    <p>This little berry's list of attributes includes a high level of antioxidant activity similar to cranberries, but more than what's been found in blueberries and strawberries. Oxygen Radical Absorbance Capacity (ORAC) values measuring the antioxidant power of acai fruit pulp/skin powder reportedly have the highest ORAC value among fruits and vegetables, or 10 times more antioxidants than red grapes.

Acai berries are low in sugar, but contain excellent amounts of calcium, fiber, and vitamin A. They also contain anthocyanin compounds such as resveratrol and cyaniding and ferulic acid, which not only give fruits and vegetables their distinct color, but also team up with flavonoids to defend the body against harmful free radicals. In fact, acai berries contain 10 to 30 times more anthocyanin power than red wine. Beneficial fatty acids such as oleic acid, one of the same oils found in olive oil, is another strong point, and healthy levels of dietary fiber keep the system functioning smoothly.

The properties contained in acai berries may help prevent health problems such as arthritis, inflammation, obesity, erectile dysfunction, neurological diseases, and allergies. Lab studies have shown them to have positive effects on ailments associated with oxidative stress, heart disease, and cancer.

Acai berries should be consumed in moderation, because they still contain sugar, even if in lower levels than other fruits. Remember, fructose may be harmful to your health in excessive amounts.</p>

