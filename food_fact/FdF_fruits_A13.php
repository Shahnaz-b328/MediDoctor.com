  <h2>What Is Cherimoya Good For?</h2>
  <p>Native to the valleys of southern Ecuador and northern Peru, cherimoya is unheard of by many Americans, since it has somewhat demanding growing preferences.1 One of the most crucial factors in cultivating cherimoya is high temperature, which thwarts the plant’s growth and fruit production. Leaf shrinking and discoloration were both observed in cherimoya trees planted under direct light.2

In the late 1800s, about 9,000 cherimoya trees were introduced to California, but most of them died due to the low temperatures, with the same happening to cherimoyas planted in Florida, with the trees producing poor-quality fruits upon harvest.3

Outside of America, cherimoya is known on nearly every continent, from Egypt to Hawaii. It's sometimes known as cherimolia or chirimolla. It is also referred to as “pap” or “tsummy” in Guatemala, and “anone” in France.4 It belongs to the same family as soursop, pawpaw and sugar apple.5

Cherimoya fruits are typically conical, with some people describing them as heart-shaped. The fruits can be easily broken, exposing its white and juicy flesh. The different varieties of cherimoya may differ in fruit shape and texture, but they all offer the same sweet flavor.

Cherimoya fruits are usually eaten by hand or scooped out with a spoon. The sweetness of the fruit is natural, and usually doesn’t require anything to accentuate it, but some people prefer adding a few drops of lime juice. It can also be added to fruit salads or made into ice cream or sherbet.6

Because of its sheer sweetness, Mark Twain described the cherimoya as “the most delicious fruit known to men</p>


    <h4>Health Benefits of Cherimoya</h4>
    <p>One of the principal components of cherimoya is its antioxidant content. In a 2010 study from Food Research International, the skin, flesh and juice of cherimoya were found to contain significant levels of antioxidant activity, with the juice having the highest percentage. Because of this, regular consumption of cherimoya may help combat numerous conditions, such as cardiovascular disease, cancer and other disorders linked to oxidative damage.8,9

Cherimoya provides high potassium levels, which helps control heart rate and blood pressure.10,11 Furthermore, it contains more minerals per weight than a lot of more common fruits, such as apples, because of its magnesium, iron and phosphorus content.12,13

In rural Mexico, cherimoya seeds are sometimes roasted, peeled and pulverized into a powder and mixed with water or milk. This mixture is then used as a cathartic or an emetic. It can also be mixed with grease and used to kill lice</p>

