    <h4>Studies Done on Cherimoya</h4>
    <p>Used in traditional Mexican medicine for its anxiety-relieving, anticonvulsant and tranquilizing properties, cherimoya was studied for possible development of medicine for depressive disorder. In a 2012 study from the Journal of Ethnopharmacology, repeated test treatments of cherimoya in a lab setting produced antidepressant-like effects in mice. It was strongly suggested by scientists that it be used in that capacity as an antidepressant medium.15

Cherimoya has also been studied for its possible role in cancer treatment. In a 2012 study from the Journal of Agricultural Science, cherimoya and avocado extracts were observed to have cytotoxic and mitodepressive properties, which may be helpful in arresting the development of cancer cells.16 However, another study found certain annona cultivars to induce Parkinson's disease-like symptoms, ostensibly due to the phytochemical annonacin content, which is also found in cherimoya seeds.</p>
