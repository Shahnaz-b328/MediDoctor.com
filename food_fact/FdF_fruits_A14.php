    <h4>What Benefits Can You Get From Citron?</h4>
    <p>While citron may look like a bumpy and unattractive lemon, the vitamins and nutrients it offers are surely striking. Citrons are loaded with properties that can boost your immune system, balance acid levels in the gut and improve digestion. Some of the benefits you can get from citrons include: 9

	Helps regulate blood pressure. Citron is rich in potassium and vitamin C, two compounds that help regulate blood circulation by reducing the strain on blood vessels.10
	Helps protect you from oxidative stress. The high antioxidant and vitamin C content in citron may help protect you from free radicals that can cause a barrage of chronic diseases.
	Works as an analgesic. A 2010 study shows that citron may be used to relieve pain because of its flavonoid and phenolic components.11
	May help fight against infections. A 2012 study shows that the peel extract of citron is an effective antibacterial and antiviral agent. It has potential in fighting off staph, E.coli and Pseudomonas aeruginosa.</p>

