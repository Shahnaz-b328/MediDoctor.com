    <p>A study published in mid-2011 reported that the acaí berry not only offers potential health benefits due to its antioxidant scavenging capacities, but also suppresses biological activity that would degrade cellular integrity.1

Research published in September 2012 reported the effects of acai berry pulp on chemically-induced urothelial bladder cancer in mice, and the incidence and multiplicity of transitional cell carcinoma (TCC). After 10 weeks of treatment, the acai berry pulp treatment was found to reduce the TCC incidence, multiplicity, and tumor cell proliferation, as well as significantly reduce DNA damage. The study authors reported that the positive acai berry pulp treatment was probably due to the fruit's antioxidant abilities.2

The purpose of another study was to evaluate how acai fruit pulp affected metabolic disorder risks. It involved 10 overweight adults taking 100 grams of acai pulp twice daily for a month. Prior to the study, each subject's blood glucose, blood pressure, cholesterol, triglycerides, and other biomarkers were measured.

At the end of the 30-day trial, researchers found reductions in fasting glucose, insulin levels, and total cholesterol; however, there was no effect on blood pressure. Scientists concluded that the acai fruit pulp reduced the levels of selected metabolic disease markers in the overweight subjects, indicating that further studies, including those on heart disease and/or type 2 diabetes, are warranted.3</p>
