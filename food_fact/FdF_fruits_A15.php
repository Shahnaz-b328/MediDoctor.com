  <h2>What Are Coconuts Good For?</h2>
  <p>While the coconut is a mainstay in the diet of nearly one-third of the planet, it's considered exotic in the Western world. Experts describe the coconut as not just a fruit, but a nut and a seed as well – a drupe. It has three layers: the outside layer is known as the exocarp, and the fibrous husk or mesocarp is inside of it. The thin, woody layer surrounding the actual coconut "meat" is the endocarp.

Since man inhabited the world, coconut trees were in existence. Coconuts have been used not only to produce fiber, fuel, and even musical instruments, but also as a traditional Asian and Pacific Island folk remedy.

For centuries, the coconut has been claimed as a potent cure for nausea, rash, fever, earache, sore throat, bronchitis, kidney stones, ulcers, asthma, syphilis, dropsy, toothache, bruises, and lice. And that's just the short list. While coconut milk is a wonderful base for all kinds of Thai dishes, coconut oil is becoming increasingly used in the U.S. for its nutritional benefits.</p>

    <h4>Health Benefits of Coconuts</h4>
    <p>Modern medicine has also found coconuts nutritionally beneficial because:

They're loaded with naturally sterile electrolytes, which feed your pH and muscle function

They have a high fiber content, which makes you feel full longer and helps regulate elimination

They're rich in lauric acid, which converts to monolaurin. Monolaurin is the compound found in breast milk that strengthens a baby’s immunity, and a great deal of research has been done to establish the ability of lauric acid to enhance immunity. This medium-chain fatty acid (MCFA) actually disrupts the lipid membranes of offending organisms.

They offer high levels of manganese, potassium, and phosphorus.</p>

