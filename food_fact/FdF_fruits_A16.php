    <h4>Studies Done on Cranberries</h4>
    <p>Studies have demonstrated the range of potential health benefits provided by cranberries. For instance, one study published in the Journal of the Science of Food and Agriculture found that cranberries may help lower the risk for cancer by inducing apoptosis and inhibiting the growth and proliferation of tumor cells.20

A review published in Molecular Nutrition and Food Research also supported the chemopreventive properties of cranberries, highlighting their ability to help inhibit the growth and spread of several types of tumors, including those in the breast, colon, prostate and lungs, possibly due to the flavonols and anthocyanin glycosides in these fruits.21

Regarding the use of cranberry extract on lower urinary tract infections, one study published in Clinics suggests that “daily dosages of 240 to 300 milliliters (8 to 10 fluid ounces) of cranberry juice cocktail can prevent 50% of the recurrences of UTIs and can reduce bacteriuria.</p>
