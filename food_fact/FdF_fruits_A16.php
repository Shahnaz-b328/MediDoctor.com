  <h2>What Are Cranberries Good For?</h2>
  <p>Known for their juicy tartness and vibrant red hue, cranberries can make for a great snack or addition to trail mix, and are ever-present in traditional Thanksgiving feasts next to the turkey roast. They’re often associated with the holidays, and for good reason: This fruit’s short harvest season is between September and October, just in time for holiday cooking.1

Native to the U.S., cranberries were regularly consumed by Native Americans and used as a food preservative, a poultice for wounds and as a dye for rugs, blankets and other fabrics.2

European settlers learned how to use cranberries from Native Americans and eventually found that they could be a valuable bartering tool. American seafarers even carried cranberries onboard for their ability to fight against scurvy.3

Fresh cranberries have a shelf life of up to two months when stored in a tightly sealed container in the refrigerator. But if you want your cranberries to keep longer, it’s advisable to freeze them instead.

First, wash the berries, then spread them out to dry in a single layer on a baking sheet. Place the frozen cranberries in an airtight container after a couple hours, then place them in the freezer. You can store these for up to a year.4

When buying juice, sauce and other cranberry products from the supermarket, make sure that they’re made from 100% organic cranberry and not loaded with added sugar, artificial flavoring or preservatives.

Because cranberries contain oxalate5,6 and can also enhance the anticoagulant capacity of certain medications,7 individuals with kidney stones and those on blood-thinning medications like warfarin should limit their intake of this fruit.8,9 Remember to consume cranberries in moderation because they contain fructose,10 which can be harmful to your health in excessive amounts.</p>


    <h4>Health Benefits of Cranberries</h4>
    <p>Cranberries, and their juice in particular, are known for their potential in managing urinary tract infections (UTIs) by keeping bacteria from attaching to the bladder and urethra.11 This same mechanism also accounts for this fruit’s ability to help lower the risk for dental caries and disease. According to a study in the Journal of the Canadian Dental Association, cranberry polyphenols may interfere with the adhesion of the Porphyromonas gingivalis bacteria, which is “the primary etiologic agent in chronic periodontitis.”12

Cranberries also contain phytonutrients with antioxidant properties, including oligomeric proanthocyanidins, anthocyanin flavonoids (which give them their bright red color13), cyanidin, peonidin and quercetin.14 A study published in Nutrition Reviews also found that these bioactive compounds may help reduce the risk for cardiovascular disease by inhibiting LDL cholesterol oxidation and platelet aggregation, lowering blood pressure levels and fighting inflammation.15

Cranberries contain high amounts of fiber,16 which may help normalize bowel movement, promote healthy bowel function, lower blood sugar and blood pressure levels, and maintain a healthy weight.17 Plus, they’re a good source of manganese and vitamin A, E and C.18 Check out the table below learn more about the nutritional values of cranberries.</p>

