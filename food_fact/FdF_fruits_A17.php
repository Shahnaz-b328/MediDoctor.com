  <h2>What Are Dates Good For?</h2>
  <p>Dates don't appear to be particularly special with their oddly wrinkled, brown exterior, but they're satisfyingly chewy and flavorful. Undoubtedly a favorite of many, dates are considered a drupe because they contain a single pit or stone at the center.1

Date palms, which produce these little beauties, were brought to America's Western coast by Spanish missionaries in the late 1700s, while other varieties were brought years later.2 In 1900, W.T. Swingle brought the first Deglet Noor offshoots, which is a variety that remains as one of the most important types of dates today. Medjool dates, which were introduced 27 years later,3 are sometimes called the “Cadillac of Dates” because of their sweet and meaty texture.4

Aside from the two varieties mentioned above, dates come in other forms that differ in color and firmness. Some of the types available in farmers markets and food stores include barhi dates, halawy dates and khadrawy dates. 5

Date palms typically start producing fruits at 3 to 5 years of age, and are considered fully mature upon reaching their 12th year.6 But aside from the commercial cultivation of dates, wild populations of the plant are still thriving, specifically in Oman and Jordan.7

Popular uses around the world include date jam, chutney, date trail mix, flavorings and roasted whole date seeds.8 The tree's terminal buds (hearts of palm) may also be eaten raw or added to salads</p>


    <h4>Health Benefits of Dates</h4>
    <p>When it comes to the number of minerals, vitamins and health-benefiting phytonutrients in dates, suffice it to say there are a lot of them.10 They're also easily digested, allowing your body to make full use of their nutritional value.

Dietary fiber in dates helps to move waste smoothly through your colon and helps prevent LDL (bad) cholesterol absorption. It may also bind with substances that can decrease biological compounds that cause cancer.12 The iron content in dates, a component of hemoglobin in red blood cells, determines the balance of oxygen in the blood.13 potassium, an electrolyte, helps control your heart rate and blood pressure. B vitamins in dates, such as the carotenoids lutein and zeaxanthin, are absorbed into the retina to maintain optimal light-filtering functions and protect against macular degeneration.

Additionally, dates contain vitamins A and K. Vitamin A protects the eyes, maintains healthy skin and mucous membranes, and even protects the lungs and mouth from developing cancer.16 Tannins, which are flavonoids as well as polyphenolic antioxidants, fight infection and inflammation and help prevent excessive bleeding (anti-hemorrhagic).17,18 Vitamin K is a blood coagulant that also helps metabolize your bones.

Copper, magnesium, manganese, zinc, niacin, pantothenic acid and riboflavin are also present in dates and provide their own unique preventive and healing functions.20

Together, these cofactors help your body metabolize carbohydrates, protein and fats. Eating dates in moderation can contribute many health benefits, such as protecting against damage to cells from free radicals, helping lower the risk of stroke, coronary heart disease and the development of colon, prostate, breast, endometrial, lung and pancreatic cancers.</p>

