    <h4>What Do Studies Show About Dragon Fruit?</h4>
    <p>Some studies have already been conducted to determine if dragon fruit plays a role in improving overall health and well-being. One example is a 2011 study from the Asian Pacific Journal of Cancer Prevention, where researchers assessed the relationship between a healthy diet containing fruits and vegetables, lifetime physical activity and oxidative DNA damage linked to prostate cancer .

The conclusion was that exercise and a diet of fresh fruits and vegetables, especially those containing lycopene like dragon fruit, were found to be protective against the disease.22

Another study published in Drug Metabolism and Disposition in 2004 aimed to learn more about the capacities of tropical fruits like dragon fruit, since there is limited information surrounding them. The study yielded positive results, and the researchers discovered that tropical fruits like dragon fruit may inhibit the activity of the human cytochrome P450.23

This substance can often be seen in the liver, but may also be found in other tissues such as in the small intestine, lungs and kidneys, and mutations of this particular substance have been associated with development of congenital glaucoma.</p>
