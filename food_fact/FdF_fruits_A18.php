  <h2>What Is Dragon Fruit Good For?</h2>
  <p>The pitaya is one of nature’s most unique plants, with a flower like an explosion of flame and a fruit like a pink rosebud. Widely known as dragon fruit, it is indigenous to Central America but is also grown and exported from several Southeast Asian countries, such as Thailand and Vietnam.1 The dragon fruit is actually one of several cactus species and, just like its relatives, has a succulent stem that provides a uniquely delicious fruit with moisture in the arid climates where it typically grows.2

Dragon fruit looks like a soft pineapple with spikes, and can have either pink, red or yellow skin and white or red flesh. The fruit also has overlaid or scale-like leaves, similar to an artichoke,3 and an abundance of small, black edible seeds. Its flavor is mildly sweet, like a blend of kiwifruit and pear, and has a crunchy texture.4

While the fruit may seem a little strange at first, it's easy to prepare. Begin by slicing the dragon fruit lengthwise. Afterward, you can remove the flesh, or slice the fruit further into quarters and peel the leathery skin. Make sure that you only eat the white part containing the seeds, and discard any remaining pink portions, as they tend to be bitter.</p>

    <h4>Health Benefits of Dragon Fruit</h4>
    <p>Dragon fruit contains a surprising number of phytonutrients.6 It is also loaded with antioxidants,7 and is home to carotene, protein, vitamin C (said to be near 10 percent of the daily recommended value), polyunsaturated (good) fatty acids and B vitamins that may be needed for carbohydrate metabolism .8,9 In addition, this tropical fruit doesn’t contain complex carbohydrates, which may allow vitamin B1 (thiamin) along with other B vitamins in the body to break down food more easily in the body.10

The dragon fruit is also a source of other nutrients like calcium that may help develop strong bones and teeth, iron that may assist in forming healthy red blood cells, and phosphorus to aid in promoting tissue and cell growth, maintenance and repair.11,12

A phytochemical called captin is present in dragon fruit too. It is typically used in medicines that may help alleviate heart problems. Other known benefits of dragon fruit include boosting the immune system, promoting quicker recovery from wounds and bruises, and reducing the risk for respiratory problems.13

Eating dragon fruit may also help the body maintain its normal function by helping eliminate toxic heavy metals14 and improving eyesight.15 Lycopene, responsible for the fruit’s red color ,16 has been linked with a lower prostate cancer risk.17 Meanwhile,

seed extracts from dragon fruit are high in polyunsaturated fats (omega-3 and omega-6 fatty acids)18 that may help reduce triglyceride levels19 and lower the risk of cardiovascular disorders.20 In some cases, oil derived from the seeds may serve as a mild laxative too.21

Make sure to consume dragon fruit in moderation because it contains fructose, a type of sugar that may be harmful to your health if consumed in excessive amounts.</p>

