  <h2>What Is Ackee Good For?</h2>
  <p>Ackee is the national fruit of Jamaica,1 but it’s actually native to the tropical forests of West Africa. It is believed that samples of the tree were brought to the Caribbean island in the 18th century and it’s been there ever since.2

The name “ackee” has African origins, and is derived from the word “Ankye” from the Twi language of Ghana. Its botanical name pays tribute to Captain William Bligh, who bought the plants from Jamaica to England in 1793 and introduced it to the Western world.3

Since its introduction to Jamaican culture, ackee has become an integral part of its history due to its role in “ackee and saltfish,” the country’s national dish.4 Today, it is exported around the world and enjoyed by countless people all over.

</p>

    <h4>What Is Ackee?</h4>
    <p> Ackee is classified as an evergreen tree, which means that it has foliage throughout the year.5 It can grow up to a height of 25 feet tall, despite having a short trunk. The crown can grow very large and bloom luminous green leaves. It typically bears fruit twice a year, with the first harvesting season from January to March, and the second from June to August.6

The fruit typically grows in clusters on the branches. It has a characteristic red skin that, when opened, exposes three large, black seeds. Great care is important when harvesting the fruit, because the arils — the yellow, fleshy portion attached to each seeds — are the only edible portions. The rest contain toxins that can cause fatal side effects.7

The taste of ackee is often described to be mild, with a buttery and creamy texture.8 Due to these characteristics, ackee is usually treated as a vegetable in cooking, as opposed to a fruit. This is why it is used in many savory dishes, because it can pair well with other vegetables, meat and fish.9</p>

