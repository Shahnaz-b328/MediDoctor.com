  <h2>What Are Grapes Good For?</h2>
  <p>When you think of a popular fruit with a myriad of uses, grapes might come to mind. Under the correct treatment and conditions, the "fruit of the vine"1 is a key ingredient to one of the most popularly consumed and widely produced beverages in the world — wine.2

Dried grapes (raisins) can be a lovely addition to salads and yogurt. Fresh, seedless grapes can be used to make grape juice, and they’re a handy snack all by themselves.

Grapes have existed since the beginning of human civilization, cultivated since the Neolithic era,3 and grown as far back as 6,000 to 8,000 years ago.4 You can find them all over the world — the estimated total global output per year is 75 million tons.5

According to the Vitis International Variety Catalogue, there are over 23,000 grape cultivars today,6 though only a few are currently used.7 Grapes are grouped into two varieties: table grapes and wine grapes.

Table grapes, usually seedless, are large with thin skins. Grown to be consumed fresh or for grape products, they are less acidic and have a lower sugar content. Wine grapes are sweeter, contain more seeds and are smaller with relatively thicker skins. They also have more juice than pulp.8

A delicious lunchbox treat, grapes also offer body-wide health benefits and may offer protection against chronic diseases, thanks to their phytochemicals.9 To maximize these, the seeds and skin contain the richest concentration of antioxidants.</p>


    <h4>Health Benefits of Grapes</h4>
    <p>Like many other plant-based foods, grapes are healthy for various reasons. In every 100-gram serving, they contain 14.6 micrograms of vitamin K and 4 milligrams of vitamin C.11 The resveratrol content represents one of the most important phytonutrients12 because of its ability to protect against colon and breast cancers,13 coronary heart disease,14 degenerative nerve disease15 and even Alzheimer's.16

Anthocyanins, another plentiful antioxidant phenolic phytochemical in red grapes, generate antidiabetic, anti-inflammatory, antimicrobial and anti-obesity activities, as noted by a 2017 study.17 Oligopeptides (peptides or amino acids) with antibacterial properties18 and catechins19 (a type of flavonoid antioxidants) found in grapes, also abound with health-protecting properties.

Grapes help inhibit “bad” LDL cholesterol20 and plaque buildup21 in the arteries. They are rich in micronutrient minerals like magnesium and calcium. Grapes have a low glycemic index and contain the hormone and antioxidant melatonin,22 as well as about 191 milligrams of potassium per 100 grams.23 Iron is particularly concentrated in raisins, with 2.59 milligrams per 100-gram serving.24

Inflammation is the basis of many life-threatening diseases. Messaging molecules (such as interleukin) and pro-inflammatory enzymes (like cyclo-oxygenase) that advance inflammation can be defused by grape intake.25

However, make sure you consume grapes in moderation, as they contain fructose, which may be harmful to your health in excessive amounts. For more about the nutrition facts of grapes, check the table below.</p>

