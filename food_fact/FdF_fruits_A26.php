    <h4>Studies Done on Grapes</h4>
    <p>In 2010, researchers reported that grapes and grape products were “clearly identified” as having the potential to positively influence and even help minimize the risk of heart disease, cancer, diseases affecting nerve cells in the brain, age-related cognitive decline, Alzheimer's disease and diabetes, with possible benefits for oral health, immune function, and antiviral activity as well. Studies attributed this to the antioxidant and flavonoid content in grapes and their ability to increase nitric oxide production.27

Grape powder was shown to have a positive effect when tested on colon cancer patients, enough for researchers to determine that grapes may have colon cancer-preventative properties as well due to their resveratrol content.</p>
