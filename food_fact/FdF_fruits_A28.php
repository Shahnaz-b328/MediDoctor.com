    <h4>Studies on Guava</h4>
    <p>In an animal study published in the International Journal of Biological Macromolecules, researchers found that guava may be useful in the treatment of Type 2 diabetes, as well as reducing the risk for hyperlipidemia, cirrhosis and cardiovascular disease. Its mechanisms of action lie in its ability to help increase your insulin sensitivity and decrease your levels of fasting glucose, total cholesterol, triglyceride and serum insulin, among others.

A more recent study published in Molecules evaluated the antidiabetic and antioxidant activities of guava leaves and found that, along with lowering fasting blood sugar level and scavenging free radicals, guava leaf extract may help inhibit hepatic, renal and pancreatic damage.</p>
