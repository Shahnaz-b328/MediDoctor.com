  <h2>What are apples good for?</h2>
  <p>Apples are a popular lunchbox snack known for their delicious, crunchy, juicy and sweet nature. Although they are often considered distinctly American, they actually originated from the mountains of Kazakhstan from the Malus sieversii tree, the progenitor of the modern apple. Interestingly, you can still find this ancient apple tree growing today.1  

European settlers brought apples to America during the 1600s and cultivation was successful right from the start. By the 19th century, apples were found everywhere and came in different forms. In 1905, the U.S. Department of Agriculture (USDA) noted that there were around 17,000 apple varieties. In actuality, it was around 14,000 because some names would overlap.2

