    <h4>Studies done on apple</h4>
    <p>One study shows that the flavonoids and nitrates in apples may help protect against cardiovascular disease15 by lowering blood pressure, as well as improving endothelial function to regulate blood flow.16 Flavonoids are one of the most important naturally occurring phytochemicals in fruits and vegetables.

Research also indicates a connection between eating apples and the reduced risk of certain cancers, specifically colorectal cancer,17 due to the high flavonoid content that releases antioxidant activity. Flavonoids work against cancer cells by influencing cellular processes such as apoptosis, cell proliferation and cell differentiation.18

Polyphenols found in apples may have benefits as well. The following benefits were observed in mice, which may translate positively to human health:

Lowered inflammation19
Improved muscle endurance20
Reduced aspirin-related gut damage21
To get the most out of your apples, make sure you eat the peel. Research shows that this part of the fruit contains the highest amount of antioxidants compared to other parts.22 Just make sure that you’re consuming organic apples to avoid pesticide contamination.</p>
