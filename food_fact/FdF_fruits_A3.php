    <h4>Health benefits of apples</h4>
    <p>Apples are high in vitamin C,4 one of the most important nutrients you should regularly attain from your diet. Studies show that vitamin C may help regulate blood pressure5 as well as reduce oxidative stress damage caused by exercise.6  Vitamin A, also found in apples, is crucial for the reproductive health of both males and females, as well as the growth of embryos.7 Increasing your intake of vitamin A may also help resist infection by boosting your immune function.8 Additionally, apples contain generous amounts of dietary fiber, which may help inhibit LDL (bad) cholesterol concentration, thereby improving cardiovascular health.9

Apples are a good source of folate, a nutrient that may help lower the risk of neural tube defects in fetuses.10 Increasing folate during pregnancy may also lower the risk of congenital heart defects in newborn children.11

Minerals found in apples include iron,  phosphorus, calcium and potassium, which are important components in helping regulate blood pressure.12 All of these elements and more demonstrate why apples can legitimately be called a “superfood.”</p>

