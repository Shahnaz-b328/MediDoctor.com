    <h4>Studies on limes</h4>
    <p>Research has shown that sun, salt and lime juice are ingredients that may provide clean, inexpensive water in areas of the world where it's unavailable. While the solar disinfection of drinking water (SODIS) method (exposing glass containers of water to six hours of sunlight) is already utilized in some African, Asian and Latin American countries, there are still challenges linked to it.

For instance, some parts of the world only have dirty water available, and lack filters that may make the water less murky, preventing the sun’s rays from stimulating the purification process and rendering this method possibly ineffective. However,  a 2012 Environmental Health Perspectives study suggests that adding tiny amounts of salt to the water causes it to adhere to and weigh down dirt particles.22

In further testing, the addition of lime juice resulted in a faster disinfecting ability, due to large quantities of psoralen compounds present in the fruit. These compounds bond with DNA strands in the presence of sunlight, thereby preventing DNA replication. Overall, results showed that the addition of a lime slurry or plain lime juice to the SODIS method helped reduced the amount of E. coli bacteria by about a million times in just 30 minutes, compared to using  the SODIS method alone, said to only be one-fourth as effective.23

In a Nigerian children's hospital, lime juice was tested as a treatment for parasites. When antimalarial medicines were given with the juice, researchers found that the combination significantly lowered parasite load. A higher percentage of subjects given both the medication and the juice also experienced complete recovery within 72 hours. Meanwhile, the treatment failed in subjects who were only given the medication without the lime juice.24

Lime juice was also found to exhibit antibacterial effects against the Vibrio cholera bacteria, as revealed in a 2000 Tropical Medicine and International Health article. According to authors, in places where cholera epidemics had occurred, particularly in Guinea-Bissau located in West Africa, the inclusion of lime juice in meals helped protect against development and transmission of the bacteria responsible for cholera.</p>
