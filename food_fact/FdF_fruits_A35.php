  <h2>What are limes good for?</h2>
  <p>Wonderful for bringing out the flavor of other foods, limes have their own distinctive citrus flavor, despite being similar to lemons.

Limes’ origins can be traced to Asia, particularly to Northeast India and Indochina,1 and eventually reached African and eastern Mediterranean countries through Arab traders. They spread to western Mediterranean countries during the 12th and 13th centuries after the Crusades.2

Christopher Columbus is believed to have brought the fruit to Haiti. From that point, Spanish missionaries brought them to Florida.3 Limes have been used to prevent scurvy, a disease that can lead to shortness of breath, pain and tooth loss.4,5

They’re also typically added to various desserts and beverages,6 and are widely grown in Brazil, Argentina, Mexico, Pakistan and India, and in the Middle East.7

Growers usually cultivate key, Tahitian (Persian) or makrut (kaffir) limes for commercial purposes. Out of these three types, the Tahitian lime is the most popular and is grown worldwide. This medium-sized, oval-shaped lime is said to have a “‘true’ lime flavor.”8

Meanwhile, key limes, which are popular because they’re used to make the famous pie of the same name, are smaller9 and more acidic.10 No matter which variety you prefer, make sure that when picking or purchasing limes, they're a deep, dark green color and are close to ripe.</p>


    <h4>Health benefits of limes</h4>
    <p>Limes contain powerful compounds like alkaloids, carotenoids, coumarins, flavonoids, phenolic acids, triterpenoids, kaempferol, apigenin, hesperetin and quercetin, to name a few.12

Limes’ vitamin C content is notable too. It was proven to aid in lowering your risk of being affected with cold and flu symptoms13 and addressing pain caused by rheumatoid arthritis.14 Eating foods containing significant amounts of vitamin C, like limes, may also help lower the risk of other health problems such as stroke,15 coronary heart disease16 and cancer.17 Researchers have also concluded that compounds called limonoids, typically found in citrus fruits such as limes,18 may decrease risk for lung, breast, stomach, colon, skin and mouth cancers.19

A 2013 animal study discovered that lime juice and peel helped decrease the risk of atherosclerosis (hardening of the arteries) among animal subjects. Researchers also highlighted that the peels were actually more effective compared to the beverage.20 In another article, lime peels were found to have antioxidant properties, showing potential in fighting free radicals that can damage the body’s cells, trigger aging and increase your risk for diseases.</p>

