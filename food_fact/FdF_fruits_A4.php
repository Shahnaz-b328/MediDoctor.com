  <h2>What Is Apricot Good For?</h2>
  <p>Ripe and golden with a luscious flavor and velvety surface – or a more intense shade when dried – apricots are closely related to peaches, plums, and nectarines. Grown on deciduous trees, they're actually a drupe, with the seed central to the surrounding fruit and each having a fairly uniform size. They're sometimes overlooked as a fruit, perhaps because there are so many fruits to choose from in today's markets.

Already well-known in Greece in 60 BC, apricots were transported to Europe by Greeks who called them “golden eggs of the sun.” Today, apricots' primary commercial operations are in the Mediterranean and Middle East, with off-season offerings from South America or New Zealand. In America, the first apricot tree was first shipped to Virginia in 1720, but orchards in the Spanish missions of California circa 1790 showed how such a climate can proliferate this fruit.

Apricots can be consumed fresh, and can also be a delicious addition to any kind of salad. Dried apricots seem to be the most desired form, and are often mixed with nuts and other dried fruits for a tasty, nutritious homemade trail mix. The pits have their own corner on the market, principally for their bitter oil.

</p>


    <h4>Health Benefits of Apricots</h4>
    <p>In fresh apricots, the highest level of vitamins is provided by vitamin A – 13% of the daily value in one fruit. This includes several beneficial carotenes, which release powerful antioxidants essential for maintaining your optimal vision, healthy skin and mucous membranes, and for protection against lung and mouth cancers.

Apricots also are a good source of vitamin C, known to ward off vision problems, promote healthy skin and mucous membranes, protect against lung and mouth cancers, and provide even more free radical-scavenging activity in your body. Flavonoids such as lutein and beta cryptoxanthins have such a high degree of benefits that they can actually help slow and even prevent age-related debilities and disease. Zeaxanthin, a carotenoid that absorbs into the retina, provides protective UV-filtering functions that may ultimately protect you against macular degeneration.</p>

