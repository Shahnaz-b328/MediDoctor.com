  <h2>What Are Avocados Good For?</h2>
  <p>Their flavor and versatility have contributed to avocados’ rise in popularity. Gonzalo Fernandez de Oviedo, a historian for a group of Spanish conquistadors traversing South America, first talked about a “seed like a peeled chestnut surrounded by a paste ‘similar to butter and of very good taste’” in his book “General History of the Indies” that was published in 1526. The seed he referred to is what we now know as avocados.1

However, these interesting fruits were already used thousands of years ago in Central and South America. Avocados were cultivated by the Caral, Mokaya and Maya peoples of Peru2 and Mexico,3,4 and consumed in the Tehuacan Valley (present day Central Mexico) nearly 10,000 years ago.

Avocado-related references were also found on Pacal tombs in Chiapas (also in Mexico), and in Aztec paintings. The Mayan calendar also considered avocado as the name of its 14th month.5

R.B. Ord, a judge from Santa Barbara, California, is the reason why avocado is now cultivated in the U.S. He acquired three avocado samplings from Mexico back in 1871 and cared for them in his garden.6

California is now responsible for producing between 75% to 92% of avocados in the U.S.,7 and the California Avocado Commission highlights that the state has 3,000 growers who cultivate the fruit across 50,000 acres.8</p>



    <h4>How to Prepare Avocados</h4>
    <p>To enjoy fresh avocados (formerly called "alligator pear" in England9), they need to be prepared well. As much as possible, peel the avocados using your hands so the antioxidants in the fruits can be preserved. Here’s a step-by-step guide from the California Avocado Commission on how to properly peel an avocado:10
Wash avocados before eating to potentially lower your risk for bacteria contamination.Cut the avocado in half lengthwise, around the seed.Cut each half further to make four avocado segments by rotating the fruit (about a 1/4 turn) and slicing it lengthwise around the seed.
Separate the four segments from each other and remove the seed your hands to peel the skin off each avocado quarter.</p>

