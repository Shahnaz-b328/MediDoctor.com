    <h4>Studies on Black Currants</h4>
    <p>Scientists investigated the inhibitory effects of black currant extract against pathogens associated with oral, nasal, and upper respiratory infectious diseases, including respiratory syncytial virus, influenza virus A and B, herpes simplex virus type 1, Streptococcus pneumoniae, and Streptococcus mutans. They found that "less than one percent concentration of extract of blackcurrant inhibited replication of (several of the diseases investigated) by over 50 percent and a 10 percent extract inhibited adsorption of these viruses onto the cell surface by over 95 percent" and "a 10 percent extract disinfected 99.8 percent of H. Influenzae type B and 78.9 percent of S. pneumoniae in 10 minutes."

The conclusion was that black currant extract has potential as a functional food for oral care.1

One study noted the dietary antioxidants, such as anthocyanins, were helpful in preventing and controlling various diseases by counteracting the imbalance of oxidative and antioxidative factors. Black currant, known to contain high amounts of anthocyanins, has recently been found to be the second most effective amongst nine different berry extracts studied for their free radical scavenging activity. The study was conducted to evaluate the potential antiproliferative effects of black currant fruit skin extract against liver cancer cells. The results indicate, for the first time, that black currant skin containing an anthocyanin-rich fraction inhibits the proliferation of liver cancer cells.2</p>
