  <h2>What Are Green Beans Good For?</h2>
  <p>A favorite pastime of the summer season is sitting on a porch swing while snapping green beans for dinner. Picking them from the garden is fun, too, whether they’re pole beans, which require a stake for support, or bush beans, which don't.1

But what are green beans, exactly? These are unripe fruits of different varieties of the Phaseolus vulgaris plant.2 The beans usually grow 8 to 20 centimeters long, with green, yellow, black or purple shades.3 Some examples of bean varieties are fava, black, lima, pinto and red kidney.4

Green beans are believed to have Mesoamerican origins, with the wild bean supposedly diversifying in South or Central America.5 According to the Food and Agriculture Organization of the United Nations, China, Myanmar and India are the top three producers of green beans as of 2017, with 91.9% of the world’s production coming from Asia.6

Unfortunately, green beans contain one of the highest pesticide contents across the board. Consumer Reports claim that eating one serving of conventionally grown green beans may be 200 times more unsafe than eating broccoli.7 This means that if you’re planning on adding this food to your diet, make sure you only purchase organic produce to minimize your exposure to these harmful chemicals.

To obtain the maximum nutrients from green beans, the George Mateljan Foundation recommends quick steaming, which consists of steaming the green beans with 2 inches of water for about seven minutes. This limits the green beans’ exposure to high temperatures, preserving heat-sensitive vitamins, such as vitamin C and vitamin B6.</p>


    <h4>Health Benefits of Green Beans</h4>
    <p>If you're looking for a vegetable with stellar amounts of antioxidants and flavonoids, green beans certainly fit the bill. Beans have abundant levels of flavanols, such as catechin, epicathechin and epigallocatechin.9 According to a 2009 study from the International Journal of Obesity, catechins, together with caffeine, help regulate body weight by controlling the storage of fat.10 The epicatechin content also offers benefits for people with diabetes by helping reduce blood glucose levels, thereby promoting better insulin sensitivity.11

Green beans are a great source of lutein and zeaxanthin, which play important roles in preserving eye health. These carotenoids may help fight macular degeneration, age-related cataracts, cognition diseases and retinopathy.12,13

Green beans contain considerable amounts of folate (vitamin B9) and other B-vitamins. Folate is well-known for its importance in fetal development and the production of red blood cells. Optimized levels of folate may significantly decrease the risk of neural tube defects in unborn children as well.14

Despite these incredible benefits, it’s worthwhile to note that raw green beans contain small amounts of lectins, compounds that serve as a defense system of plants. When consumed in high amounts, lectins may cause numerous disorders, such as problems with gut permeability and the microbiome. To protect yourself from these possibly harmful conditions, be sure to only consume green beans in moderate amounts. For more information on how to reduce the lectins from green beans, read “Limit the lectins.</p>

