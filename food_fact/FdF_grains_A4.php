    <h4>Studies on Green Beans</h4>
    <p>Studies have shown that flavonoids play an important role in reducing the risk of illnesses like cancer and heart disease, while offering a wide range of pharmacological benefits, such as antioxidant, anti-inflammatory, antimicrobial, neuroprotective and antidiabetic effects, to name a few.15 

A 2006 animal study from The Journal of Nutrition found that a diet rich in green and yellow vegetables may significantly lower the risk for atherosclerosis. The researchers noted that a diet rich in broccoli, peas, carrots and green beans may reduce atherosclerosis by up to 38%.16

As for food processing and nutrient retention, a 2004 study observed the effect of commercial processing on green beans’ vitamin C and phenolic compound levels. It was found that typical processing did not affect vitamin C and folate levels that much, but reduced the antioxidant activity by as much as 20%.</p>
