    <h4>Studies on Green Peas</h4>
    <p>A 2012 review published in The British Journal of Nutrition highlighted green peas’ potential benefits, particularly against selenium or iron deficiency-related diseases. The research noted that the polyphenolics in this food “may have antioxidant and anticarcinogenic activity, saponins which may exhibit hypocholesterolaemic and anticarcinogenic activity, and galactose oligosaccharides which may exert beneficial prebiotic effects in the large intestine.”31

One of the phytonutrients in peas, a polyphenol called coumestrol, has been recognized for its ability to protect from stomach cancer. A Mexico City-based study indicated that daily consumption of green peas along with other legumes lowers your risk of stomach cancer, especially when 2 or more milligrams of coumestrol are ingested. (A one-cup serving of peas contains around 10 milligrams.)</p>
