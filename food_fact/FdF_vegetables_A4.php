  <h2>What Is Arugula Good For?</h2>
  <p>If you ever see a salad green referred to as “rocket,” it’s simply another name for arugula, or roquette in French.1

A type of Brassicaceae along with kale and cauliflower,2 arugula’s delightfully pungent leaves have been cultivated in Europe and the Mediterranean since time was recorded.3 As such, arugula is a perennial favorite in Italian cooking.

Unremarkable in appearance, arugula is often added to mesclun mixes, where it adds a delicious zest. While the young, paler leaves have a mild flavor — good for fresh dishes like salad and pesto — the older, darker leaves have a bit of pepper to them,4 making them stand out in soups and pizza toppings.</p>



    <h4>Health Benefits of Arugula</h4>
    <p>Arugula derives a lot of nutritional value from its cruciferous family roots, such as antioxidant benefits from glucosinolates and detoxifying power from enzymes. It’s an excellent source of fiber, vitamins A, C (to boost the immune system) and K (for bone strength), folate, calcium, iron, magnesium, phosphorus, potassium, and manganese.5

Arugula also provides high levels of protein, riboflavin, thiamin, zinc, vitamin B6, copper and pantothenic acid (vitamin B5).6 Its flavonoid content has multiple benefits: to lower inflammation,7 prevent cholesterol from sticking to arteries,8 manage high blood pressure levels,9 increase blood flow and improve blood vessel function.10</p>

