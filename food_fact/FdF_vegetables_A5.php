    <h4>Studies done on asparagus</h4>
    <p>The active components of asparagus have been the subject of numerous scientific studies, mainly focusing on steroidal saponin content. In a 1997 study from Planta Medica, researchers isolated two oligofurostanosides from asparagus seeds, which were found to have cytotoxic effects on human leukemia cells.8This coincides with a 2010 study published in Phytochemistry Reviews, where triterpene and steroid saponins triggered apoptosis in tumor cells and cytoskeleton disintegration.9

Asparagus officinalis extracts may also protect against oxidative stress and liver and kidney damage as reported in a 2018 animal study from Toxicology Reports. Wistar rats were co-administered bisphenol A (BPA) and asparagus officinalis extract (AOE), with BPA inducing oxidative stress in both the liver and kidneys. AOE provided the rats with liver and kidney tissue protection, significantly lowering the effects of BPA.10

Asparagus stem extracts were found to have dermatological benefits as well. A 2018 study from Environmental Health and Preventive Medicine shows that asparagus extracts may help prevent photo aging by inducing the expression of HSP70 during UV-B irradiation.11

In agriculture, growth inhibition of asparagus has been considered a major agricultural problem in Japan. Due to this, asparagus plants were diagnosed to discover the reason. Still a controversial topic as experts discuss the possibility of chemical or foodborne pathogens, the study published on Microbes and Environments, which was conducted using soil studies in several fields in 2007 and 2008, concluded that the growth inhibition was due to continuous cropping of asparagus.</p>
