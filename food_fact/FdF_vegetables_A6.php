  <h2>What Are Beet Greens Good For?</h2>
  <p>Grown in the Mediterranean region as far back as 2,000 B.C., beet cultivation spread to Babylonia in the eighth century, then to China around 850 A.D.

Beets, along with their greens, belong to the Goosefoot family, known as Chenopodiaceae. Within the botanical family, beet greens are factored alongside spinach, Swiss chard, quinoa, lamb's quarter, and a number of other wild plants, which means that beet greens can be placed in the “dark, leafy” category. Because they are a cool season crop, beets grow quickly and can survive almost freezing temperatures, making them a favorite of northern gardeners. Beets and their greens also enjoy a longer than normal growing season.

It’s best to use beet greens within two or three days after refrigeration. Enjoy beet greens by themselves as a salad or with other leafy vegetables, or sauté them in a bit of olive oil or balsamic vinegar and salt for a delicious side dish.

Here’s a great tip: if you find yourself with too many beet greens, don’t throw them away. Freeze them and use for soup stock.

Read more about edible wild plants.

</p>


    <h4>Health Benefits of Beet Greens</h4>
    <p>Besides supplying good amounts of protein, phosphorus, and zinc, beet greens are also a great source of fiber. Packed with antioxidants, they’re high in vitamin B6, magnesium, potassium, copper, and manganese, and low in fat and cholesterol. Based on a 2,000 calorie diet, daily values of beet greens contain: 220% of vitamin A, 60% of vitamin C, 16% of calcium, and 15% of iron.

As if that weren’t enough, studies have also shown that the vitamin K in beet greens contains blood clotting properties, helps ward off osteoporosis, works with calcium to boost bone strength, and may also play a role in fighting Alzheimer’s disease. Beet greens have a higher iron content than spinach, and a higher nutritional value than the beetroot itself.

The vitamin A content in beet greens helps strengthen the immune system and stimulates production of antibodies and white blood cells. The beta-carotene in vitamin A is a known antioxidant that can fight the effects of free radicals in the body along with cancer and heart disease. Doctors often recommend vitamin A to patients at risk of developing night blindness.</p>
