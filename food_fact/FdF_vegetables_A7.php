    <h4>Studies Done on Beets</h4>
    <p>When a study of 27 of the most commonly used vegetables in the U.S. was conducted to determine their cellular antioxidant activity (CAA) and oxygen radical absorbance capacity (ORAC) values, beets, along with broccoli and red pepper, were shown to be the highest. Consuming beets may lead to a reduced risk of developing chronic diseases, such as cancer and cardiovascular disease.1

Research at the London School of Medicine and Bart’s Hospital in London, and by Russian research specialist Mikhail Tombak revealed new information about beets. Studies showed young beet leaves to have a higher concentration of iron than spinach, and beets to be rich in phytonutrients with detoxification, antioxidant, and anti-inflammatory properties, able to control hypertension and heart attack risks. Beets are one of the best sources of lutein and zeaxanthin, highly beneficial for eye health. Lab studies on tumor cells in humans showed beets to significantly curb tumor growth. Daily consumption of beet juice reportedly boosts blood flow to the brain, which in turn restricts frontal lobe degeneration. Beets also contain betaine, which enhances serotonin production in the brain.2</p>
