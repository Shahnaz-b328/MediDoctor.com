  <h2>What are bell peppers good for?</h2>
  <p>Bell peppers are some of the most versatile vegetables available, no matter where you go in the world. They make a delicious sauté with onions,1 and may be added to salads,2 soups3 and casseroles.4 They can be stuffed,5 lightly grilled,6 added to a simple and delicious dip7 or simply sliced for a fresh, flavorful and crunchy snack.

Compared to other peppers, bell peppers aren’t known for a high level of spiciness. Red bell peppers tend to be sweeter, as are some yellow and orange varieties.8

The heat intensity of peppers is measured in Scoville heat units (SHU). Green, yellow or red bell peppers score a zero on the scale, jalapeño peppers have around 3,500 to 8,000 and habañeros are at 150,000 to 300,000 units. There are also other peppers you can include in your dishes for both flavor and color, such as banana, shishito, cayenne, and serrano.</p>



    <h4>Health benefits of bell peppers</h4>
    <p>Bell peppers have a fair share of health-boosting vitamins and minerals. Regular consumption of vitamin C-rich foods, like bell peppers (which may contain more than twice the vitamin C of an orange10) may help protect your body against scurvy,11 boost the immune system,12 decrease inflammation,13 reduce risk for heart disease14 and scavenge harmful free radicals in the body.15

Other nutritional benefits of bell peppers come from nutrients like thiamin (vitamin B1), niacin (vitamin B3), folate, magnesium and potassium.16

A 2005 study published in the Journal of Food Composition and Analysis showed that vitamin K, which is abundant in bell peppers, may help synthesize some proteins that may positively affect blood coagulation.17 Vitamin K may also play a role in protecting your bones against osteoporosis.18,19 People with reduced bone density have lower levels of this nutrient.20  Interestingly enough, sautéed peppers contain higher amounts of vitamin K than raw peppers.21

While bell peppers’ health benefits are numerous, it’s important to note that they’re part of the nightshade plant family22 along with eggplants and potatoes,23 and contain high amounts of lectins. These are plant proteins that can attach to cell membranes.24

Lectins are antinutrients that can resist digestion, cause negative shifts in the balance of your gut bacteria, trigger inflammation, increase your risk for abnormal clotting and potentially predispose you to leptin resistance. However, you can reduce the lectin content in bell peppers by removing their seeds prior to cooking, since these antinutrients tend to be concentrated in the seeds or skin of fruits and vegetables.</p>
